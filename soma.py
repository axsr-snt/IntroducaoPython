# Crie um programa que leia dois números e mostre o resultado da soma entre eles


num1 = int(input("Digite o primeiro número: "))

num2 = int(input("Digite o segundo número: "))

soma = num1 + num2

print("\nA soma é igual a ", soma)

print("\nO resultado da soma entre {} e {} é {}" .format(num1, num2, soma))